package oops;

public class Statcclass {

//passing parameters along with  one static class 

	public int add(int a , int b){
		
		int c=a+b;
		return c;
	}
	
	public double sub(double x , double y){
		
		double z=x-y;
		return z;
	}
	public static String completename(String e ,String f){
		
		 String fullname = e+f;
		return fullname;
	}
}
